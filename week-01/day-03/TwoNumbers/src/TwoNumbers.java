public class TwoNumbers {
    public static void main(String[] args){
        int numberA = 22;
        int numberB = 13;
        double numberC = (double)22 / (double)13;
        System.out.println(numberA + numberB);
        System.out.println(numberA - numberB);
        System.out.println(numberA * numberB);
        System.out.println(numberC);
        System.out.println(numberA / numberB);
        System.out.println(numberA % numberB);
    }
}
