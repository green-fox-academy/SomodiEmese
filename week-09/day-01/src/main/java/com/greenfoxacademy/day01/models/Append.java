package com.greenfoxacademy.day01.models;

public class Append {
    String appended;
    String error;

    public Append() {
        this.appended = appended;
        this.error = error;
    }

    public String getError() {
        return error;
    }

    public void setError(String error) {
        this.error = error;
    }

    public String getAppended() {
        return appended;
    }

    public void setAppended(String appended) {
        this.appended = appended;
    }
}
